WITH ordered_items AS (
  SELECT
    id,
    ROW_NUMBER() OVER (
      PARTITION BY framework_id
      ORDER BY
        CASE WHEN code ~ '^[0-9]+(.[0-9]+)?$' THEN 0 ELSE 1 END,
        CASE
          WHEN code ~ '^[0-9]+(.[0-9]+)?$' THEN split_part(code, '.', 1)::int
        END NULLS LAST,
        CASE
          WHEN code ~ '^[0-9]+(.[0-9]+)?$' AND position('.' IN code) > 0 THEN split_part(code, '.', 2)::numeric
        END NULLS LAST,
        code
    ) AS sort_order
  FROM framework_items
)
UPDATE framework_items fi
SET sort_order = ordered_items.sort_order
FROM ordered_items
WHERE fi.id = ordered_items.id;

